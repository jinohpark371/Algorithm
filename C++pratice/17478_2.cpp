#include <iostream>
using namespace std;
int n;

void _var(const char* str, int cnt) { 
    for (int i = 0; i < cnt; i++) cout << "____";
    cout << str;
}

void solve(int cnt) {
    _var("\"재귀함수가 뭔가요?\"\n", cnt); 
    if (cnt == n) {
        _var("\"재귀함수는 자기 자신을 호출하는 함수라네\"\n", cnt);
    }
    else {
        _var("\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.\n", cnt);
        _var("마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.\n", cnt);
        _var("그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\"\n", cnt);

        solve(cnt + 1);
    }
    _var("라고 답변하였지.\n", cnt);

}

int main() {
    cin >> n;

    solve(0);
}